	public function after($event = array()) {

	    if (isset($event['create'])) {
	        switch ($event['create']) {
	            case 'regions':
	                App::uses('ClassRegistry', 'Utility');
	                $region = ClassRegistry::init('Region');
	                $region->create();
	                $region->save(
	                    array('Region' =>
	                        array('name' => 'Latin America')
	                    )
	                );
	                break;
	            case 'countries':
	                App::uses('ClassRegistry', 'Utility');
	                $region = ClassRegistry::init('Country');
	                $region->create();
	                $region->save(
	                    array('Region' =>
	                        array('name' => 'Latin America'
	                        	  '')
	                    )
	                );
	                break;
			}
    	}
	}

